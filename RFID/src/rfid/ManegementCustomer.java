    private void jToggleButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton3ActionPerformed
        // TODO add your handling code here:
        
        if(sdtJbl.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Vui lòng nhập số điện thoại");
        }else if(tenkhachJlb.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Vui lòng nhập tên khách");
        }else{
            Customer c = new Customer(sdtJbl.getText(),tenkhachJlb.getText());
            CustomerBUS.setCustomer(c);
            JOptionPane.showMessageDialog(null, "Thêm Khách Hàng Thành Công");
            customer = CustomerBUS.getList();
            outModelKhachHang(model, customer);
        }
        
    }//GEN-LAST:event_jToggleButton3ActionPerformed

