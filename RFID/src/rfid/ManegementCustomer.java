    public void outModelKhachHang(DefaultTableModel model , ArrayList<Customer> customer) // Xuất ra Table từ ArrayList
    {
        model = (DefaultTableModel) customerTbl.getModel();
        Vector data;
        model.setRowCount(0);
        for(Customer c: customer)
        {
            
            data = new Vector();
            data.add(c.getCustomer_id());
            data.add(c.getName());
            model.addRow(data);
            
        }

        customerTbl.setModel(model);
        model.fireTableDataChanged();
    }

