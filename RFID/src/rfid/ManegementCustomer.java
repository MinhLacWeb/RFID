    private void customerTblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_customerTblMouseClicked
        // TODO add your handling code here:
            DefaultTableModel dmkh = (DefaultTableModel) customerTbl.getModel();
            int row = customerTbl.getSelectedRow();
            sdtJbl.setText((String) dmkh.getValueAt(row,0));
            tenkhachJlb.setText((String) dmkh.getValueAt(row,1));
    }//GEN-LAST:event_customerTblMouseClicked

    private void jToggleButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton2ActionPerformed
        // TODO add your handling code here:
        
        
        if(customerTbl.getSelectedRow() != -1) {
               // remove selected row from the model
               
               DefaultTableModel dmkh = (DefaultTableModel) customerTbl.getModel();
                int row = customerTbl.getSelectedRow();
                String customer_id = (String) dmkh.getValueAt(row,0);
                CustomerBUS.deleteCustomer(customer_id);
                customer.remove(customerTbl.getSelectedRow());
               JOptionPane.showMessageDialog(null, "Xóa Khách Hàng thành Công");
            }else{
                JOptionPane.showMessageDialog(null, "Bạn chưa chọn dòng để xóa");
            }
        
        customer = CustomerBUS.getList();
        outModelKhachHang(model, customer);
        
       
    }//GEN-LAST:event_jToggleButton2ActionPerformed

